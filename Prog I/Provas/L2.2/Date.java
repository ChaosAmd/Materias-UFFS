class Date{
    public int hora;
    public int minutes;
    public int segundo; 

    public void setHora(int hora){
        this.hora = hora;
    }

    public int getHora(){
        return this.hora;
    }

    public void setMinutos(int minutos ){
        this.minutes = minutos;
    }

    public int getMinutos(){
        return this.minutes;
    }

    public void setSegundos(int segundo){
        this.segundo = segundo;
    }
    public int getSegundos(){
        return this.segundo;
    }
    
    
}